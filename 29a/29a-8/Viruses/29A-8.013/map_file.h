#include "defines.h"

int map_file(Infect *file)
{
	/* map whole target file */
	file->mmap.start=mmap(0,file->file_size,PROT_READ | PROT_WRITE,MAP_PRIVATE,file->fd_f,0);
	
	if(file->mmap.start!=MAP_FAILED)
		return true;
	
	ERROR(map_file);
	return false;
}
