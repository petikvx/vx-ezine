void dump(FILE*outf,
          BYTE* buf,   /* (d1d2d3c1c2c3) x count        */
          int len)     /* size of buffer, in BYTE's     */
{
  int count = len / 6;   /* total # of entries */

  for(DWORD x = 0, y, ptr = 0; ptr < len; ptr += 6)
  {

    /* extract info, reversed reordering on-the-fly.
       can you do the same in assembly ? ;-) */
#define Y(n)   ((n)%6)
#define X(n)   ((n)/12)
#define G0(n)   ( (buf[(Y(n)*2+0)*(count/2)+X(n)]&0xf0)      |(buf[(Y(n)*2+1)*(count/2)+X(n)]>>4))
#define G1(n)   (((buf[(Y(n)*2+0)*(count/2)+X(n)]&15  )<<  4)|(buf[(Y(n)*2+1)*(count/2)+X(n)]&15))
#define G(n)    ((((n)/6)&1)?G1(n):G0(n))

    x += ((G(ptr+0)<<24) | (G(ptr+1)<<16) | (G(ptr+2)<<8));
    y = x + ((G(ptr+3)<<24) | (G(ptr+4)<<16) | (G(ptr+5)<<8)) - 1;

    if (ptr > 0 && x == 0) break; // last padding entry or some error

    /* now, x/y = min/max ip */

    fprintf(outf,"%08x,%08x,%d.%d.%d.%d,%d.%d.%d.%d\n",
      x,
      y,
      x>>24,(x>>16)&0xff,(x>>8)&0xff,x&0xff,
      y>>24,(y>>16)&0xff,(y>>8)&0xff,y&0xff );

  }

}

